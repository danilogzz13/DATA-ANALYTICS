-- ============================================================================
-- REFERENTIAL INTEGRITY
-- ============================================================================

%sql
SELECT COUNT(*) AS PROCUREMENT_WITHOUT_VENDOR
FROM SUPPLY_CHAIN.GOLD.FACT_PROCUREMENT P
LEFT JOIN SUPPLY_CHAIN.GOLD.DIM_VENDOR V
    ON P.VENDOR_ID = V.VENDOR_ID
WHERE V.VENDOR_ID IS NULL;

%sql
SELECT COUNT(*) AS PROCUREMENT_WITHOUT_MATERIAL
FROM SUPPLY_CHAIN.GOLD.FACT_PROCUREMENT P
LEFT JOIN SUPPLY_CHAIN.GOLD.DIM_MATERIAL M
    ON P.MATERIAL_ID = M.MATERIAL_ID
WHERE M.MATERIAL_ID IS NULL;

%sql
SELECT COUNT(*) AS SALES_WITHOUT_CUSTOMER
FROM SUPPLY_CHAIN.GOLD.FACT_SALES S
LEFT JOIN SUPPLY_CHAIN.GOLD.DIM_CUSTOMER C
    ON S.CUSTOMER_ID = C.CUSTOMER_ID
WHERE C.CUSTOMER_ID IS NULL;

%sql
SELECT COUNT(*) AS SALES_WITHOUT_MATERIAL
FROM SUPPLY_CHAIN.GOLD.FACT_SALES S
LEFT JOIN SUPPLY_CHAIN.GOLD.DIM_MATERIAL M
    ON S.MATERIAL_ID = M.MATERIAL_ID
WHERE M.MATERIAL_ID IS NULL;

-- ============================================================================
-- INVALID NUMERIC VALUES
-- ============================================================================

%sql
SELECT COUNT(*) AS INVALID_PROCUREMENT_ROWS
FROM SUPPLY_CHAIN.GOLD.FACT_PROCUREMENT
WHERE ORDER_QTY < 0
OR RECEIVED_QTY < 0
OR UNIT_PRICE < 0;

%sql
SELECT COUNT(*) AS INVALID_TRANSPORTATION_ROWS
FROM SUPPLY_CHAIN.GOLD.FACT_TRANSPORTATION
WHERE DISTANCE_KM < 0
OR FREIGHT_COST < 0;

%sql
SELECT COUNT(*) AS INVALID_SALES_ROWS
FROM SUPPLY_CHAIN.GOLD.FACT_SALES
WHERE QUANTITY < 0
OR UNIT_PRICE < 0
OR DISCOUNT < 0
OR SALES_AMOUNT < 0;

-- ============================================================================
-- BUSINESS IMPACT VALIDATION
-- ============================================================================

%sql
SELECT
    COUNT(*) AS TOTAL_ROWS,
    COUNT(DISTINCT CONCAT(
        SALES_ORDER_ID,
        '-',
        SALES_LINE_ID
    )) AS UNIQUE_SALES_LINES,
    SUM(
        CASE
            WHEN GROSS_MARGIN < 0 THEN 1
            ELSE 0
        END
    ) AS NEGATIVE_MARGIN_ROWS,
    ROUND(AVG(MARGIN_PERCENT),2) AS AVG_MARGIN_PERCENT
FROM SUPPLY_CHAIN.GOLD.FACT_BUSINESS_IMPACT;

%sql
SELECT
    SALES_ORDER_ID,
    SALES_LINE_ID,
    CUSTOMER_NAME,
    MATERIAL_DESC,
    SALES_REVENUE,
    MATERIAL_COST,
    FREIGHT_COST,
    LANDED_COST,
    GROSS_MARGIN,
    MARGIN_PERCENT
FROM SUPPLY_CHAIN.GOLD.FACT_BUSINESS_IMPACT
WHERE GROSS_MARGIN < 0
ORDER BY GROSS_MARGIN ASC
LIMIT 100;

-- ============================================================================
-- SALES RECONCILIATION
-- ============================================================================

%sql
SELECT
    ROUND(SUM(SALES_AMOUNT),2) AS FACT_SALES_REVENUE
FROM SUPPLY_CHAIN.GOLD.FACT_SALES
WHERE ACTIVE_FLAG = 'Y';

%sql
SELECT
    ROUND(SUM(SALES_REVENUE),2) AS BUSINESS_IMPACT_REVENUE
FROM SUPPLY_CHAIN.GOLD.FACT_BUSINESS_IMPACT;

-- ============================================================================
-- COST RECONCILIATION
-- ============================================================================

%sql
SELECT
    ROUND(SUM(MATERIAL_COST),2) AS TOTAL_MATERIAL_COST,
    ROUND(SUM(FREIGHT_COST),2) AS TOTAL_FREIGHT_COST,
    ROUND(SUM(LANDED_COST),2) AS TOTAL_LANDED_COST
FROM SUPPLY_CHAIN.GOLD.FACT_COST_ANALYSIS;

%sql
SELECT
    ROUND(SUM(SALES_REVENUE),2) AS TOTAL_REVENUE,
    ROUND(SUM(MATERIAL_COST),2) AS TOTAL_MATERIAL_COST,
    ROUND(SUM(FREIGHT_COST),2) AS TOTAL_FREIGHT_COST,
    ROUND(SUM(LANDED_COST),2) AS TOTAL_LANDED_COST,
    ROUND(SUM(GROSS_MARGIN),2) AS TOTAL_GROSS_MARGIN
FROM SUPPLY_CHAIN.GOLD.FACT_BUSINESS_IMPACT;
