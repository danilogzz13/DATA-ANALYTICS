-- ============================================================================
-- DUPLICATE BUSINESS KEYS
-- ============================================================================

%sql
SELECT
    MATERIAL_ID,
    COUNT(*) AS DUPLICATE_COUNT
FROM SUPPLY_CHAIN.GOLD.DIM_MATERIAL
GROUP BY MATERIAL_ID
HAVING COUNT(*) > 1;

%sql
SELECT
    VENDOR_ID,
    COUNT(*) AS DUPLICATE_COUNT
FROM SUPPLY_CHAIN.GOLD.DIM_VENDOR
GROUP BY VENDOR_ID
HAVING COUNT(*) > 1;

%sql
SELECT
    PO_ID,
    PO_LINE_ID,
    COUNT(*) AS DUPLICATE_COUNT
FROM SUPPLY_CHAIN.GOLD.FACT_PROCUREMENT
GROUP BY
    PO_ID,
    PO_LINE_ID
HAVING COUNT(*) > 1;

%sql
SELECT
    SHIPMENT_ID,
    COUNT(*) AS DUPLICATE_COUNT
FROM SUPPLY_CHAIN.GOLD.FACT_TRANSPORTATION
GROUP BY SHIPMENT_ID
HAVING COUNT(*) > 1;

%sql
SELECT
    CUSTOMER_ID,
    COUNT(*) AS DUPLICATE_COUNT
FROM SUPPLY_CHAIN.GOLD.DIM_CUSTOMER
GROUP BY CUSTOMER_ID
HAVING COUNT(*) > 1;

%sql
SELECT
    SALES_ORDER_ID,
    SALES_LINE_ID,
    COUNT(*) AS DUPLICATE_COUNT
FROM SUPPLY_CHAIN.GOLD.FACT_SALES
GROUP BY
    SALES_ORDER_ID,
    SALES_LINE_ID
HAVING COUNT(*) > 1;

